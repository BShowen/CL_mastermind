
The computer will generate a random code. The player will then make a guess as to what that code is. The computer will provide feedback to the player giving clues as to what the player got correct, incorrect, and partially correct. 

Classes: Player, Computer 
Modules: GameDispaly 

Player: Will decide a guess and tell the Computer what that guess is. 

Computer: Create a RandomCode. Receive a guess from Player and compare it to the RandomCode. Send feedback to the Player (in the form of a GameDisplay) giving the player some Clues. 
